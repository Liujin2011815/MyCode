//
//	This file defines the enum command maps that are used to implement the 
//  XXX::ToCommand and XXX:FromResponse functions that translate defined values 
//  to/from instrument commands/responses.
//
//	Each entry in the map is of the following form:
//	
//	ENUM_COMMAND(<enum value>, <command and response list>, <model or family>)
//
//	For example:
//
//	ENUM_COMMAND(KTMAWG_VAL_TRIGGER_SOURCE_INTERNAL, "INT, INT1, INT2", "ModelA")
//	
//	The <command and response list> is a comma-separated list of strings that will
//	be used to send commands to the instrument when the associated enum value is
//	used in a function or attribute command or response.  The first item in the
//	list (e.g. "INT", in the example above) is the command that will be sent to the
//	instrument when the associated enum value is used.  The other items in the list
//	are additional responses that the driver will recognize as alternatives that
//	the instrument may send in a response string that all should be interpreted
//	as mapping to the same enum value.
//
//	Each enum value may have different commands and responses based on the instrument
//	model or family.  The third parameter in the ENUM_COMMAND macro indicates the
//	specific model or family associated with the command mappings.  An empty string
//	indicates the default set of mappings to use if a model-specific mapping is
//	not specified in the map.
//  
//  This file also defines the command map for ViBoolean translation.  The macros
//  for this map work identically to the enum maps, except that the macro values
//  (VI_TRUE and VI_FALSE) are implicit in the definition of the command map itself.
//
#pragma once

